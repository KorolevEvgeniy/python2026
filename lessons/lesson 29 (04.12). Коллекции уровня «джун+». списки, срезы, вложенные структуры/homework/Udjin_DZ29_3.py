tasks = ["купить билеты", "собрать рюкзак", "спланировать маршрут"]   # Начальный список дел
print("Начальный список дел:", tasks)
tasks.extend(["проверить запас воды", "упаковать еду"])  # Добавляем в конец списка два дела
print("1. После добавления в конец:", tasks)
tasks.insert(2, "зарядить гаджеты") # Вставляем в середину (после второго элемента)
print("2. После вставки в середину:", tasks)
if "купить билеты" in tasks: # 3. Удаляем дело «купить билеты» (если оно есть)
    tasks.remove("купить билеты")
    print("3. После удаления 'купить билеты':", tasks)
else:
    print("3. Дело 'купить билеты' не найдено в списке")
# Перемещаем последнее дело в начало списка
if tasks:  # Проверяем, что список не пустой
    last_task = tasks.pop()  # Удаляем последний элемент и сохраняем его
    tasks.insert(0, last_task)  # Вставляем его в начало
    print("4. После перемещения последнего дела в начало:", tasks)
else:
    print("4. Список пуст, нечего перемещать")